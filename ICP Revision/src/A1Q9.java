import java.util.Scanner;
public class A1Q9 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println(5); // 5
        System.out.println((float)25/6); // 4.16
        System.out.println(5+'6'); // 5 + 54 = 59
        System.out.println(5+7+'9'); // 12 + 57 = 69
        System.out.println("92"+7+5); // 9275
        System.out.println(2+"9"); // 29
        sc.close();
    }
}
